print("WELCOME TO SID'S PRIME NUMBER CHECKER!")
print("---------------------------------------------------------------------------------------------------")
print("Remember, prime numbers can only be WHOLE numbers which are bigger than 1!")
print("---------------------------------------------------------------------------------------------------")
a = int(input("What is the number you would like to check?\n"))

if a > 1:
    for b in range(2, a):
        if a % b == 0:
            print("---------------------------------------------------------------------------------------------------")
            print(a, "is not a prime number!", b, "x", a//b, "=", a)
            print("---------------------------------------------------------------------------------------------------")
            break
    else:
        print("-------------------------------------------------------------------------------------------------------")
        print(a, "is a prime number! It cannot be divided by anything except for one and itself!")
        print("-------------------------------------------------------------------------------------------------------")

else:
    print("-----------------------------------------------------------------------------------------------------------")
    print(a, "is not a prime number! All prime numbers have to be greater than one!")
    print("-----------------------------------------------------------------------------------------------------------")